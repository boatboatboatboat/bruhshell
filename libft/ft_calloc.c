#include <stdlib.h>
#include "libft.h"

void	*ft_calloc(t_size count, t_size size)
{
	void	*result;

	result = malloc(count * size);
	if (result == NULL)
		return (NULL);
	ft_bzero(result, count * size);
	return (result);
}
